"""
Parser — reads Excel/CSV files and produces chunks for embedding.

TWO KINDS OF CHUNKING (this confuses everyone at first):

1. PANDAS CHUNKING (RAM management):
   "Read 1000 rows at a time so we don't run out of memory."
   This is about your SERVER not crashing. A 500k-row file is read in
   500 batches of 1000 rows. Memory stays flat at ~50MB.

2. RAG CHUNKING (semantic units):
   "Group 20 rows together as one chunk for embedding."
   This is about SEARCH QUALITY. Each chunk becomes one vector in ChromaDB.
   - Too small (1 row): the embedding is noisy, lacks context
   - Too large (500 rows): the embedding is too broad, matches everything vaguely
   - 20 rows: sweet spot for tabular data — specific enough to match,
     broad enough to have context

HOW THEY WORK TOGETHER:
   pandas reads 1000 rows into RAM
   → we split those 1000 rows into 50 chunks of 20 rows each
   → each chunk gets column headers prepended
   → chunks are sent to the embedder
   → pandas reads the NEXT 1000 rows
   → repeat until file is done

WHAT YOU'RE LEARNING:
- pandas chunked reading (chunksize parameter)
- Generator functions for memory-efficient data processing
- Why tabular data needs different chunking than regular text
- Sheet handling for multi-sheet Excel files
- Metadata attachment — what info travels with each chunk
"""

from pathlib import Path
from dataclasses import dataclass, field

import pandas as pd

from core.config import settings


@dataclass
class Chunk:
    """
    One chunk of data ready for embedding.

    WHY A DATACLASS:
    A dataclass is like a dictionary but with named fields and type checking.
    Instead of chunk["text"] (which could be misspelled as chunk["txet"] with
    no error), you get chunk.text (which your editor autocompletes and checks).

    WHY THESE FIELDS:
    - text: the actual content that gets embedded. Includes column headers +
      row data so the embedding model understands the meaning.
    - metadata: everything ChromaDB stores alongside the vector. This is how
      you filter search results later — "only search in sheet 'Q4 Sales'"
      or "only show results from rows 100-200."
    """

    text: str
    metadata: dict = field(default_factory=dict)

    # metadata looks like:
    # {
    #     "file_id": "abc123",
    #     "file_name": "sales_2024.xlsx",
    #     "sheet_name": "Q4",
    #     "row_start": 40,
    #     "row_end": 59,
    #     "column_names": "Name|Sales|Region|Date",
    #     "num_rows": 20,
    # }


def parse_file(file_path: Path, file_id: str) -> list[Chunk]:
    """
    Main entry point. Detects file type and delegates to the right parser.

    Args:
        file_path: Path to the uploaded file.
        file_id: Unique identifier for this file (used in metadata).
                 Generated by the upload endpoint so we can track which
                 chunks came from which file.

    Returns:
        List of Chunk objects ready for embedding.

    WHY NOT ASYNC:
    pandas is synchronous — it blocks while reading. In the pipeline (file 5),
    we'll run this in a thread pool using asyncio.to_thread() so it doesn't
    block the event loop. This is the standard pattern for wrapping synchronous
    libraries in async code.
    """
    suffix = file_path.suffix.lower()

    if suffix == ".csv":
        chunks = _parse_csv(file_path, file_id)
    elif suffix in (".xlsx", ".xls"):
        chunks = _parse_excel(file_path, file_id)
    else:
        raise ValueError(
            f"Unsupported file type: '{suffix}'. "
            f"Supported types: .csv, .xlsx, .xls"
        )

    summary = _build_summary_chunk(file_path, file_id, chunks)
    if summary:
        chunks.insert(0, summary)

    return chunks


